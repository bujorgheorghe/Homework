public class Car {

     private String CountryOfCar;
     private String Model;
     private String Maker;

     public void setCountryOfCar(String a){
         CountryOfCar=a;
     }
     public void setModel(String b){
         Model = b;
     }
     public void setMaker(String c){
         Maker = c;
     }

    public String getCountryOfCar() {
        return CountryOfCar;
    }
    public String getModel() {
        return Model;
    }
    public String getMaker() {
        return Maker;
    }


}
